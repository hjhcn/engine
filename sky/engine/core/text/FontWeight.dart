part of dart.sky;

/// The thickness of the glyphs used to draw the text
enum FontWeight {
  /// Thin, the least thick
  w100,

  /// Extra-light
  w200,

  /// Light
  w300,

  /// Normal / regular / plain
  w400,

  /// Medium
  w500,

  /// Semi-bold
  w600,

  /// Bold
  w700,

  /// Extra-bold
  w800,

  /// Black, the most thick
  w900
}
