part of dart.sky;

/// A linear decoration to draw near the text
enum TextDecoration {
  /// Do not draw a decoration
  none,

  /// Draw a line underneath each line of text
  underline,

  /// Draw a line above each line of text
  overline,

  /// Draw a line through each line of text
  lineThrough
}
